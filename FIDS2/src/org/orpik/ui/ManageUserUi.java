    private void btnManageUsersAddNewUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnManageUsersAddNewUserActionPerformed
        int result = 10;
        String firstName = "";
        String lastName = "";
        String middleName = "";
        String userName = "";
        char[] passwordChar = {};
        String password = "";
        String gender = "";
        String email = "";
        String organisationName = "";
        String roleName = "";
        String jobFunctionName = "";
        String languageName = "";
        //Get values
        firstName = txtAddUserFirstName.getText();
        lastName = txtAddUserLastName.getText();
        middleName = txtAddUserMiddleName.getText();
        userName = txtAddUserUsername.getText();
        passwordChar = pwdAddUserPassword.getPassword();
        password = new String(password);
        gender = rbtAddUserMale.isSelected() ? "Male" : "Female";
        email = txtAddUserEmail.getText();
        organisationName = cboAddUserOrganisation.getSelectedItem().toString();
        roleName = cboAddUserUserRole.getSelectedItem().toString();
        jobFunctionName = cboAddUserJobFunction.getSelectedItem().toString();
        languageName = cboAddUserWorkingLanguage.getSelectedItem().toString();

        //Validate

        // Add new user
        if (btnManageUsersAddNewUser.getText().equalsIgnoreCase("Save")) {
            // Insert user details to database
            result = manageUser.saveNewUser(firstName, lastName, middleName, userName, password, gender, email, organisationName,
                    roleName, jobFunctionName, languageName);
            if (result > 0) {
                result = JOptionPane.showConfirmDialog(null, "New user saved successfully.\nDo you wish to add more users?",
                        "More Users", JOptionPane.YES_NO_OPTION);
                //Re-load all users 
                manageUser.loadUsers(tblManageUsersAllUsers, tbmUsers);
            } else {
                JOptionPane.showMessageDialog(null, "Error encountered saving new user.\nSeek assistance from system administrator",
                        "Error Saving User", JOptionPane.WARNING_MESSAGE);
            }
            //Ask user if he wants to add more users
            //result = JOptionPane.showConfirmDialog(null, "Do you wish to add more users?", "More Users", JOptionPane.YES_NO_OPTION);
            if (result == 0) {
                //Reset wizard

                //Set button text to Add User
                btnManageUsersAddNewUser.setText("Next");
                //Set mnemonic to A
                btnManageUsersAddNewUser.setMnemonic('N');
                //Set icon to Next
                btnManageUsersAddNewUser.setIcon(new javax.swing.ImageIcon(getClass().getResource("/org/orpik/images/Next_24x24.png")));
                //Remove user details panel
                pnlManageUsersAddNewUserMain.remove(pnlManageUsersAddUserLoginDetails);
                // Load next panel
                pnlManageUsersAddNewUserMain.add(pnlManageUsersAddUserPersonalDetails, java.awt.BorderLayout.CENTER);
            } else {
                //Reset wizzard
                //Set button text to Add User
                btnManageUsersAddNewUser.setText("Next");
                //Set mnemonic to A
                btnManageUsersAddNewUser.setMnemonic('N');
                //Set icon to Next
                btnManageUsersAddNewUser.setIcon(new javax.swing.ImageIcon(getClass().getResource("/org/orpik/images/Next_24x24.png")));
                //Remove user details panel
                pnlManageUsersAddNewUserMain.remove(pnlManageUsersAddUserLoginDetails);
                // Load next panel
                pnlManageUsersAddNewUserMain.add(pnlManageUsersAddUserPersonalDetails, java.awt.BorderLayout.CENTER);
                //Dispose dialog
                dlgManageUsersAddNewUser.dispose();
            }
        } else if (btnManageUsersAddNewUser.getText().equalsIgnoreCase("Next")) {
            //Add back button
            pnlManageUsersAddUserBottom.add(btnManageUsersBack);
            pnlManageUsersAddUserBottom.revalidate();
            //Set button text to Add User
            btnManageUsersAddNewUser.setText("Save");
            //Set mnemonic to A
            btnManageUsersAddNewUser.setMnemonic('A');
            //Set icon to Save
            btnManageUsersAddNewUser.setIcon(new javax.swing.ImageIcon(getClass().getResource("/org/orpik/images/Save_24x24.png")));
            //Remove user details panel
            pnlManageUsersAddNewUserMain.remove(pnlManageUsersAddUserPersonalDetails);
            // Load next panel
            pnlManageUsersAddNewUserMain.add(pnlManageUsersAddUserLoginDetails, java.awt.BorderLayout.CENTER);
        }
        pnlManageUsersAddNewUserMain.revalidate();
        pnlManageUsersAddNewUserMain.repaint();
    }//GEN-LAST:event_btnManageUsersAddNewUserActionPerformed

